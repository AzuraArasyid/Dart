void main() {
  // 1. Keliling Persegi
  int sisi = 10;
  int keliling = sisi * 4;
  print(keliling);

  // 2. Luas Lingkaran
  int jari2 = 10;
  double luas = 3.14 * jari2 * jari2;
  print(luas);

  // 3. Volume Balok
  int panjang = 10;
  int lebar = 5;
  int tinggi = 12;
  int volume = panjang * lebar * tinggi;
  print(volume);
}
